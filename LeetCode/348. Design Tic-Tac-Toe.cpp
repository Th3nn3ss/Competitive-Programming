class TicTacToe {
public:
    /** Initialize your data structure here. */
    vi r, c;
    int d1, d2, n;
    TicTacToe(int n) {
        r.resize(n, 0);
        c.resize(n, 0);
        d1 = 0;
        d2 = 0;
        this->n = n;
    }
    
    /** Player {player} makes a move at ({row}, {col}).
        @param row The row of the board.
        @param col The column of the board.
        @param player The player, can be either 1 or 2.
        @return The current winning condition, can be either:
                0: No one wins.
                1: Player 1 wins.
                2: Player 2 wins. */
    int move(int row, int col, int player) {
        int p = (player == 2) ? -1 : 1;
        r[row] += p;
        c[col] += p;
        if(row == col)  
            d1 += p;
        if(row + col == n - 1)
            d2 += p;
        if(r[row] == n || c[col] == n || d1 == n || d2 == n || r[row] == -1*n || c[col] == -1*n || d1 == -1*n || -1*d2 == n)
            return player;
        return 0;
    }       
};

