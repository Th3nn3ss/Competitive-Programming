class ZigzagIterator {
    int c = 0;
    vi ans;
public:
    ZigzagIterator(vector<int>& v1, vector<int>& v2) {
        int p = 0, q = 0;
        while(p < v1.size() && q < v2.size())
        {
            if(ans.size() % 2)
                ans.pb(v2[q++]);
            else
                ans.pb(v1[p++]);
        }
        while(p < v1.size())
            ans.pb(v1[p++]);
        while(q < v2.size())
            ans.pb(v2[q++]);
    }
    int next() {
        return ans[c++];
    }

    bool hasNext() {
        return (c < ans.size());
    }
};

