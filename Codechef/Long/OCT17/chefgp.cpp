#include <bits/stdc++.h>
using namespace std;
int main()
{
	int t;
	cin>>t;
	while(t--)
	{
		string s,ss,ss1,ss2;
		long a=0,b=0,c,d,x,y,i,ff;
		cin>>ss;
		fflush(stdin);
		cin>>x>>y;

		for(i=0;i<ss.length();i++)
		{
			if(ss[i]=='a')
				a++;
			else
				b++;
		}
		//cout<<a<<" k "<<b<<"\n";	
		if(a>b)
		{
			ff=(a%x)?1:(0);
			if(((a/x)+ff)<=(b+1))
			{
				c=a%(b+1);
				d=a/(b+1);
				for(i=0;i<(d+1);i++)
					ss1+='a';
				for(i=0;i<(d);i++)
					ss2+='a';
				for(i=0;i<(b);i++)
				{
					if((c>0))
						s+=ss1;
					else
						s+=ss2;
					c--;
					s+='b';
				}
				if(c>0)
					s+=ss1;
				else
					s+=ss2;
			}
			else
			{
				c=a/x;
				d=b;
				for(i=0;i<(x);i++)
					ss1+='a';
				for(i=0;i<(a%x);i++)
					ss2+='a';
				for(i=0;i<((a/x)+ff)-1;i++)
				{
					s+=ss1;
					if(d>0)
						s+='b';
					else
						s+='*';
					d--;
				}
				if(a%x)
					s+=ss2;
				else
					s+=ss1;
			}
		}
		else if(a<b)
		{
			ff=(b%y)?1:(0);
			if(((b/y)+ff)<=(a+1))
			{
				c=b%(a+1);
				d=b/(a+1);
				for(i=0;i<(d+1);i++)
					ss1+='b';
				for(i=0;i<(d);i++)
					ss2+='b';
				for(i=0;i<(a);i++)
				{
					if((c>0))
						s+=ss1;
					else
						s+=ss2;
					c--;
					s+='a';
				}
				if(c>0)
					s+=ss1;
				else
					s+=ss2;
			}
			else
			{
				c=b/y;
				d=a;
				for(i=0;i<(y);i++)
					ss1+='b';
				for(i=0;i<(b%y);i++)
					ss2+='b';
				for(i=0;i<((b/y)+ff)-1;i++)
				{
					s+=ss1;
					if(d>0)
						s+='a';
					else
						s+='*';
					d--;
				}
				if(b%y)
					s+=ss2;
				else
					s+=ss1;
			}	
		}
		else
		{
			ss1+='a';
			ss1+='b';
			for(i=0;i<a;i++)
				s+=ss1;
		}
		cout<<s<<"\n";
	}
}

			